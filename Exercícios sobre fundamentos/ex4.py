#escreva algo em uma váriavel e de várias informações sobre essa váriavel
x=input('Escreva algo ')
print('O tipo da váriavel é ',type(x))
print('É um número? ',x.isalnum())
print('É uma letra do alfabeto? ',x.isalpha())
print('É um valor alpha numérico? ',x.isalnum())
print('Está em caixa maior? ',x.islower())
print('Está em caixa menosr? ',x.isupper())
print('É um número decimal? ',x.isdecimal())
